import express from 'express';
import cors from 'cors';
import helmet from 'helmet';
import morgan from 'morgan';
import rateLimit from 'express-rate-limit';
import dotenv from 'dotenv';

import authRoutes from './routes/auth';
import albumRoutes from './routes/albums';
import imageRoutes from './routes/images';
import slideshowRoutes from './routes/slideshows';
import { errorHandler } from './middleware/errorHandler';
import { connectDatabase } from './utils/database';

dotenv.config();

const app = express();
const PORT = process.env.PORT || 3001;

// ãƒ¬ãƒ¼ãƒˆåˆ¶é™
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15åˆ†
  max: 100, // ãƒªã‚¯ã‚¨ã‚¹ãƒˆåˆ¶é™
  message: 'Too many requests from this IP, please try again later.',
});

// ãƒŸãƒ‰ãƒ«ã‚¦ã‚§ã‚¢
app.use(helmet());
app.use(cors());
app.use(morgan('combined'));
app.use(limiter);
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true, limit: '10mb' }));

// é™çš„ãƒ•ã‚¡ã‚¤ãƒ«é…ä¿¡ï¼ˆãƒ­ãƒ¼ã‚«ãƒ«ã‚¹ãƒˆãƒ¬ãƒ¼ã‚¸ã®å ´åˆã®ã¿ï¼‰
if (process.env.STORAGE_TYPE !== 's3') {
  app.use('/uploads', express.static('uploads'));
}

// ãƒ«ãƒ¼ãƒˆ
app.use('/api/auth', authRoutes);
app.use('/api/albums', albumRoutes);
app.use('/api/images', imageRoutes);
app.use('/api/slideshows', slideshowRoutes);

// ãƒ˜ãƒ«ã‚¹ãƒã‚§ãƒƒã‚¯
app.get('/health', (req, res) => {
  res.json({ status: 'OK', timestamp: new Date().toISOString() });
});

// ã‚¨ãƒ©ãƒ¼ãƒãƒ³ãƒ‰ãƒªãƒ³ã‚°
app.use(errorHandler);

// ãƒ‡ãƒ¼ã‚¿ãƒ™ãƒ¼ã‚¹æŽ¥ç¶šã¨ã‚µãƒ¼ãƒãƒ¼èµ·å‹•
const startServer = async () => {
  try {
    await connectDatabase();
    app.listen(PORT, () => {
      console.log(`ðŸš€ Server running on port ${PORT}`);
    });
  } catch (error) {
    console.error('Failed to start server:', error);
    process.exit(1);
  }
};

startServer();

